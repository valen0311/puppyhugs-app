package com.puppyhugs.model;

public class Proveedor {

    private Long id;

    // Campos requeridos por HU-5
    private String razonSocial;
    private String identificacionFiscal; // Criterio HU-5: Debe ser único
    private String direccion;
    private String telefono;
    private String correoElectronico; // Criterio HU-5: Debe ser único
    private String tipoProveedor;

    // --- Constructor ---
    public Proveedor() {
        // Constructor vacío para JSON
    }

    // --- Getters y Setters (Necesarios) ---

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getRazonSocial() {
        return razonSocial;
    }

    public void setRazonSocial(String razonSocial) {
        this.razonSocial = razonSocial;
    }

    public String getIdentificacionFiscal() {
        return identificacionFiscal;
    }

    public void setIdentificacionFiscal(String identificacionFiscal) {
        this.identificacionFiscal = identificacionFiscal;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    public String getTelefono() {
        return telefono;
    }

    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }

    public String getCorreoElectronico() {
        return correoElectronico;
    }

    public void setCorreoElectronico(String correoElectronico) {
        this.correoElectronico = correoElectronico;
    }

    public String getTipoProveedor() {
        return tipoProveedor;
    }

    public void setTipoProveedor(String tipoProveedor) {
        this.tipoProveedor = tipoProveedor;
    }
}